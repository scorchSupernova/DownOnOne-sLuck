var count=3;
var tot=0;

function b1(){
    if(count>0){
    var rand=Math.floor((Math.random()*10)+1);
    if(rand%2!=0) {
        ++tot;
        alert("Your point has been increased by +1");
    }
    else{
        --count;
        if(count>1) alert("You have "+count+" lives remaining!");
        else if(count==1) alert("You have "+count+" life remaining!");
        else document.getElementById("dd").innerHTML=tot;
    }}
}
